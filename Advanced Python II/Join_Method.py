# join() Method in Python
# The join() method combines all elements of an iterable (such as a list)
# into a single string using the specified separator.

# List of strings
a = ["Sayanjit", "Rohan", "Shubham"]

# Join all names using "::" as the separator
final = "::".join(a)

# Print the resulting string
print(final)

# Output:
# Sayanjit::Rohan::Shubham