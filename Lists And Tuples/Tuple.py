# A tuple is an immutable data type in python. It is similar to a list but cannot be modified after creation. Once a tuple is created, you cannot add, remove, or change its elements. Tuples are defined using parentheses () and can contain elements of different data types.

a = (1,45,342,3424,False, "Rohan", "Shivam")
print(a)
print(type(a))

a1 = (1,) # tuple with only one element needs a comma
print(a1)
print(type(a1))